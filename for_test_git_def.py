# простая функция

def sumAB(a: int, b:int):
    '''
    функция возвращает сумму двух чисел a + b
    :param a:
    :param b:
    :return:
    '''
    return a + b

def multiplicationAB(a: int, b:int):
    '''
    функция возвращает произведение двух чисел a * b
    :param a:
    :param b:
    :return:
    '''
    return a * b

def divisionAB(a: int, b:int):
    '''
    функция возвращает частное двух чисел a / b
    :param a:
    :param b:
    :return:
    '''
    return a / b
